CREATE TABLE LOCATION(
	ID      	INTEGER NOT NULL ,
	CITY    	VARCHAR2(50) ,
	POPULATION  INTEGER,
	PRIMARY KEY(ID)
);
INSERT INTO LOCATION(CITY,ID,POPULATION)
SELECT DISTINCT(AOLDATA.LOCATION.CITY), MIN(AOLDATA.LOCATION.ZIP), MAX(AOLDATA.LOCATION.POPULATION) /*Popualtion muzss sp√§ter aus der FBI Datenbank geholt werden*/
FROM AOLDATA.LOCATION
GROUP BY AOLDATA.LOCATION.CITY
ORDER BY CITY;


